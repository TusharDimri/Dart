void main() {
  // switch () { case ...... break; case ...... break; case ...... break}

  switch (2) {
    case 5:
      print("5");
      break;
    case 4:
      print("4");
      break;
    case 3:
      print("3");
      break;
    default: print("Invalid Number");
  }

  // Above is a basic example of switch case statements.
  // Above example is basic but we can use switch case for multiple purposes.
}
