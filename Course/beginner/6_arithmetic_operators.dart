main(){

  // Arithmetic Operators in Dart :-
  // + , - , * , / , %
  var num1 = 10;
  var num2 = 5;

  var add = num1 + num2;
  var sub = num1 - num2;
  var product  = num1 * num2;
  var div = num1 / num2; // Quotient  = 2
  var mod = num1 % num2; // Remainder = 0


  print("Sum = $add ,Subtraction = $sub , Product = $product , Division = $div"
      ", Modulus = $mod ");

  var apples = 15.5;
  var bananas = 16.6;

  var cost = apples + bananas ;
  print("Total Cost is Rs. $cost");

}